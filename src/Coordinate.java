
public class Coordinate {

	private int row;
	private int column;
	public Coordinate(int row, int column) {
		this.row = row;
		this.column = column;
	}

//Getter Methods:
	public int getRow()
	{
		return row;
	}
	
	public int getColumn()
	{
		return column;
	}
	
//Setter Methods:
	public void setRow(int row)
	{
		this.row = row;
	}
	
	public void setColumn(int column)
	{
		this.column = column;
	}
	

}
